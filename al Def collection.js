            document.getElementById("question1-button").addEventListener("click", function() {
              // 目的の要素の座標を取得する
              var question1Element = document.getElementById("question1");
              var question1Position = question1Element.getBoundingClientRect().top + window.pageYOffset;
            
              // スクロール位置を設定する
              window.scrollTo({ top: question1Position, behavior: 'smooth' });
            });
